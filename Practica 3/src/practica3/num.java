package practica3;

public class num 
{
    // clase que tiene un numero n que incrementamos para obtener el numero de combinaciones totales
    
    private int n;
    
    public num()
    {
        n = 0;
    }
    
    public void sumNum()
    {
        n++;
    }
    
    public int getNum()
    {
        return n;
    }
}